#!/bin/bash
# ============================================================================
# DURDOM entrypoint — запускается при старте контейнера на Vast
# ============================================================================
# Что делает:
#   1. Запускает sshd (для отладки)
#   2. Запускает onstart.sh (качает модели если их нет)
#   3. Запускает ComfyUI на 0.0.0.0:8188 (FOREGROUND — не выходит из контейнера)
# ============================================================================

set -e

echo "═══════════════════════════════════════════"
echo "DURDOM ComfyUI Container — стартуем"
echo "═══════════════════════════════════════════"

# ─── SSH ───────────────────────────────────────────────────────────────────
# Vast добавляет публичные ключи в /root/.ssh/authorized_keys через переменную
mkdir -p /root/.ssh
chmod 700 /root/.ssh
if [ -n "$SSH_PUBLIC_KEY" ]; then
    echo "$SSH_PUBLIC_KEY" >> /root/.ssh/authorized_keys
fi
[ -f /root/.ssh/authorized_keys ] && chmod 600 /root/.ssh/authorized_keys

# Стартуем sshd в фоне
service ssh start || /usr/sbin/sshd -D &

# ─── Provisioning (загрузка моделей) ───────────────────────────────────────
echo "Запускаю onstart.sh..."
/onstart.sh

# ─── ComfyUI ───────────────────────────────────────────────────────────────
echo ""
echo "═══════════════════════════════════════════"
echo "Запускаю ComfyUI на 0.0.0.0:8188"
echo "═══════════════════════════════════════════"

cd /workspace/ComfyUI
exec python main.py --listen 0.0.0.0 --port 8188
